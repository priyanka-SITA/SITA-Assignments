/*Problem 10: Count the Number of Elements
Write a function that takes an array of numbers as an argument and returns the number of elements in the array.
Example:
For the input array [5, 2, 8, 12, 3], the output should be 5.*/
var arr = [1, 4, 6, 7, 3];
var count = 0;
for (i = 0; i <= arr.length; i++) {
  count++;
}
console.log(count);
